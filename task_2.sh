echo "Текущий PATH: $PATH"

if [ -z "$1" ]; then
    echo "Ошибка: Не указана директория для добавления в PATH"
    exit 1
fi

if [ ! -d "$1" ]; then
    echo "Ошибка: Директория '$1' не существует"
    exit 1
fi

# Временное добавление в PATH для текущего сеанса
export PATH=$PATH:$1
echo "Директория '$1' добавлена в PATH"
echo "Обновлённый PATH: $PATH"

# Постоянное добавление в .bashrc
echo "export PATH=\"\$PATH:$1\"" >> ~/.bashrc
echo ""
echo "Команда добавлена в ~/.bashrc"

echo ""
echo "Изменения в текущем сеансе терминала не сохраняются после его закрытия. Каждый новый терминал получает свежие настройки"

echo ""
echo "Для применения постоянных изменений выполните:"
echo "source ~/.bashrc"
echo "Или перезапустите терминал"






